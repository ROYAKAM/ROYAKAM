package CampoDePruebas;


public class CampoDePruebas { 
    public static void main(String... args) { 
        int a = Integer.parseInt(args[0]); 
        if (a % 2 == 0) { // is even 
            //Display EVEN 
            System.out.println("EVEN"); 
        } else { 
            //Display ODD 
            System.out.println("ODD"); 
        } 
    } 
} 

